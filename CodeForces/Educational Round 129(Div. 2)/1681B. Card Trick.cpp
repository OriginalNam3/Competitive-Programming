const int mod = 1e9+7;
const int INF = INT_MAX >> 1;
int add(int a, int b) {return (1LL * a + b) % mod;}
int mul(int a, int b) {return (1LL * a * b) % mod;}

void solve(){
	int n, m; 
	cin >> n;
	vector<int> a(n);
	for (int &x: a) cin >> x;
	
	cin >> m;
	vector<int> b(m);
	for (int &x: b) cin >> x;
	int cur = 0;
	for (int i = 0; i < m; i++){
		cur += b[i];
		cur %= n;
	}
	
	cout << a[cur % n] << "\n";
}

int main () {
    ios::sync_with_stdio(false); cin.tie(0); cout.tie(0);
    int t;
    cin >> t;
    while (t--) solve();
}
